package modulo;

public class ForLoopChips {
	/*
	 * 
	 * 1 frito
	 * 2 chito
	 * 3 dorito
	 * */
	
	public static void main(String[] args) {
for (int i = 1; i <4 ; i++) {
System.out.print(i);
if (i==1) {
	System.out.println(" frito");
}
else if (i==2) {
System.out.println(" chito");	
}if (i==3) {
System.out.println(" dorito");	
}
	

}	
	
	
}
}
